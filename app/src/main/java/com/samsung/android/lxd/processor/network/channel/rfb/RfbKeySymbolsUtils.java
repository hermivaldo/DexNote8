    public int getRfbKeySymbol(KeyEvent keyEvent) {
        int codePointAt = Character.codePointAt(keyEvent.getCharacters(), 0);
        if (Character.getType(codePointAt) == 8) {
            return unicodeToRfbKeySymbol(getCombining(codePointAt));
        }
        if (Character.isDefined(codePointAt)) {
            return unicodeToRfbKeySymbol(codePointAt);
        }
        return 0;
    }

    public int unicodeToRfbKeySymbol(int i) {
        if ((i >= 32 && i <= KeycodeConstants.KEYCODE_MEDIA_PLAY) || (i >= KeycodeConstants.KEYCODE_NUMPAD_ENTER && i <= 255)) {
            return i;
        }
        if (this.mRfbKeySymbolMap.containsKey(Integer.valueOf(i))) {
            return ((Integer) this.mRfbKeySymbolMap.get(Integer.valueOf(i))).intValue();
        }
        return (-16777216 & i) == 0 ? 16777216 | i : 0;
    }

    public int getCombining(int i) {
        return this.mCombiningKeyMap.containsKey(Integer.valueOf(i)) ? ((Integer) this.mCombiningKeyMap.get(Integer.valueOf(i))).intValue() : 0;
    }

